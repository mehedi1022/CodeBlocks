import java.util.Scanner;

public class First {

    public static void main(String[] args) {

        int num1, num2;
        num1 = 12;
        num2 = 15;
        // equal to
        System.out.println(num1 == num2);
        // not equal to
        System.out.println(num1 != num2);
        //is greater than 
        System.out.println(num1 > num2);
        // is less than
        System.out.println(num1 < num2);
        //is greater than or equal to 
        System.out.println(num1 >= num2);
        //is less than or equal to 
        System.out.println(num1 <= num2);
    }
}